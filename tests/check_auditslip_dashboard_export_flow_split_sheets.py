bot_spec = importlib.util.spec_from_file_location("auditslip_bot", ROOT / "auditslip_bot.py")
assert bot_spec and bot_spec.loader
bot_mod = importlib.util.module_from_spec(bot_spec)
sys.modules["auditslip_bot"] = bot_mod
bot_spec.loader.exec_module(bot_mod)

db_path = Path(os.environ["AUDITSLIP_DB"])
bot1 = bot_mod.AuditslipBot(token="TOKEN1", db_path=db_path, dry_run=True, bot_key="bot1", company_name="บริษัท 1")
bot2 = bot_mod.AuditslipBot(token="TOKEN2", db_path=db_path, dry_run=True, bot_key="bot2", company_name="บริษัท 2")
bot1.init_db(); bot2.init_db()
base = {
    "sender_name": "Uploader",
    "status": "success",
    "slip_date_display": "22/05/26",
    "slip_date_iso": "2026-05-22",
    "slip_time": "10:00",
    "transferor_name": "ลูกค้า",
    "recipient_name": "ร้านค้า",
    "from_bank": "KBANK",
    "to_bank": "SCB",
    "to_account": "SHARED-ACC-789",
    "amount": 100.0,
    "fee": 0.0,
}
bot1.save_slip({**base, "id": "DEP1", "bot_key": "bot1", "company_name": "บริษัท 1", "chat_id": "CHAT_DEP", "chat_title": "บริษัท 1 ฝาก/เติมมือ", "message_id": 101, "file_id": "FDEP", "amount": 111.0, "reference_no": "RDEP"})
bot1.save_slip({**base, "id": "WDR1", "bot_key": "bot1", "company_name": "บริษัท 1", "chat_id": "CHAT_WDR", "chat_title": "บริษัท 1 ถอน", "message_id": 102, "file_id": "FWDR", "from_account": "SHARED-ACC-789", "to_account": "DEST-WDR", "amount": 222.0, "reference_no": "RWDR"})
